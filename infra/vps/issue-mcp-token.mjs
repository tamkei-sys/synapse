/**
 * Issue (or rotate) the VPS MCP API token and write /home/deploy/synapse/env/mcp.env.
 *
 * Run ON the VPS (node_modules resolved through apps/api):
 *
 *   /home/deploy/synapse/runtime/node/bin/node \
 *     /home/deploy/synapse/app/infra/vps/issue-mcp-token.mjs <workspace-slug>
 *
 * Token mechanics mirror apps/mcp/scripts/setup-local-mcp.mjs: 20 random
 * bytes → lowercase base32 with the `synapse_` prefix; the DB stores only the
 * SHA-256 hex hash + the last-8 suffix. Existing tokens with the same label
 * are revoked first, so re-running rotates cleanly.
 *
 * The plaintext token is written ONLY to mcp.env (0600) — stdout shows the
 * suffix, never the value.
 */
import { randomUUID, webcrypto } from 'node:crypto';
import { readFileSync, writeFileSync } from 'node:fs';
import { createRequire } from 'node:module';

const require = createRequire('/home/deploy/synapse/app/apps/api/package.json');
const pg = require('pg');

const slug = process.argv[2];
if (!slug) {
  console.error('usage: issue-mcp-token.mjs <workspace-slug>');
  process.exit(1);
}

const ENVDIR = '/home/deploy/synapse/env';
const readEnvVal = (file, key) => {
  const text = readFileSync(`${ENVDIR}/${file}`, 'utf8');
  const m = text.match(new RegExp(`^${key}=(.*)$`, 'm'));
  return m ? m[1] : null;
};

const DATABASE_URL = readEnvVal('api.env', 'DATABASE_URL');
if (!DATABASE_URL) {
  console.error('DATABASE_URL not found in api.env — run setup.sh first');
  process.exit(1);
}
const TYPESENSE_URL = readEnvVal('api.env', 'TYPESENSE_URL');
const TYPESENSE_API_KEY = readEnvVal('api.env', 'TYPESENSE_API_KEY');
const SYNC_INTERNAL_SECRET = readEnvVal('sync.env', 'SYNC_INTERNAL_SECRET');
const SYNC_INTERNAL_PORT = readEnvVal('sync.env', 'SYNC_INTERNAL_PORT') ?? '1235';

const PREFIX = 'synapse_';
const B32 = 'abcdefghijklmnopqrstuvwxyz234567';
function genToken() {
  const bytes = webcrypto.getRandomValues(new Uint8Array(20));
  let bits = 0;
  let value = 0;
  let out = '';
  for (const b of bytes) {
    value = (value << 8) | b;
    bits += 8;
    while (bits >= 5) {
      out += B32[(value >> (bits - 5)) & 31];
      bits -= 5;
    }
  }
  if (bits > 0) out += B32[(value << (5 - bits)) & 31];
  return PREFIX + out;
}
async function sha256Hex(s) {
  const buf = await webcrypto.subtle.digest('SHA-256', new TextEncoder().encode(s));
  return [...new Uint8Array(buf)].map((b) => b.toString(16).padStart(2, '0')).join('');
}

const LABEL = 'vps-claude-code';

const client = new pg.Client({ connectionString: DATABASE_URL });
await client.connect();

const { rows } = await client.query(
  `select w.id as workspace_id, m.user_id from workspace w
   join workspace_member m on m.workspace_id = w.id and m.role = 'owner'
   where w.slug = $1`,
  [slug],
);
if (rows.length !== 1) {
  console.error(`expected exactly 1 owner for slug "${slug}", got ${rows.length}`);
  process.exit(1);
}

await client.query(
  `update api_token set revoked_at = now() where label = $1 and revoked_at is null`,
  [LABEL],
);

const plaintext = genToken();
const hash = await sha256Hex(plaintext);
await client.query(
  `insert into api_token (id, workspace_id, user_id, token_hash, suffix, label, scopes, created_at)
   values ($1,$2,$3,$4,$5,$6,$7, now())`,
  [randomUUID(), rows[0].workspace_id, rows[0].user_id, hash, plaintext.slice(-8), LABEL, [
    'read',
    'write',
  ]],
);
await client.end();

const lines = [
  '# Generated by infra/vps/issue-mcp-token.mjs — never commit.',
  '# Consumed by infra/vps/mcp-stdio.sh (SSH-stdio MCP server).',
  `DATABASE_URL=${DATABASE_URL}`,
  `SYNAPSE_API_TOKEN=${plaintext}`,
];
if (TYPESENSE_URL) lines.push(`TYPESENSE_URL=${TYPESENSE_URL}`);
if (TYPESENSE_API_KEY) lines.push(`TYPESENSE_API_KEY=${TYPESENSE_API_KEY}`);
if (SYNC_INTERNAL_SECRET) {
  lines.push(`SYNC_INTERNAL_URL=http://127.0.0.1:${SYNC_INTERNAL_PORT}`);
  lines.push(`SYNC_INTERNAL_SECRET=${SYNC_INTERNAL_SECRET}`);
}
writeFileSync(`${ENVDIR}/mcp.env`, `${lines.join('\n')}\n`, { mode: 0o600 });

console.log(
  `issued MCP token …${plaintext.slice(-8)} (label ${LABEL}, workspace ${slug}) → ${ENVDIR}/mcp.env`,
);
process.exit(0);
